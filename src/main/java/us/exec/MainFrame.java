    private void BtnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCrearActionPerformed
        String names = NombreTxt.getText();
        String email = EmailTxt.getText();
        String phone = TelefTxt.getText();
        request = new Server("POST", "http://localhost:8080/api/users", "Content-Type: application/json", names + "," + email + "," + phone);
        String response = request.PostUser(names, email, phone);
        Salida.setText(response);
    }//GEN-LAST:event_BtnCrearActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        int id = Integer.parseInt(IdTxt.getText());
        request = new Server("DELETE", "http://localhost:8080/api/users", "Content-Type: application/json", "", id);
        String response = request.DeleteUserById(id);
        Salida.setText(response);
    }//GEN-LAST:event_BtnEliminarActionPerformed

    private void BtnLeerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLeerActionPerformed
        request = new Server("GET", "http://localhost:8080/api/users", "Content-Type: application/json", "");
        String response = request.GetUsers();
        Salida.setText(response);
    }//GEN-LAST:event_BtnLeerActionPerformed

    private void BtnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnActualizarActionPerformed
        int id = Integer.parseInt(IdTxt.getText());
        String names = NombreTxt.getText();
        String email = EmailTxt.getText();
        String phone = TelefTxt.getText();
        String body = names + "," + email + "," + phone;
        request = new Server("PUT", "http://localhost:8080/api/users", "Content-Type: application/json", body, id);
        String response = request.PutUserById(body, id);
        Salida.setText(response);
    }//GEN-LAST:event_BtnActualizarActionPerformed

